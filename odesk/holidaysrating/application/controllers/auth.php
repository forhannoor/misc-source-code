				'phone'      => $this->input->post('phone1') . '-' . $this->input->post('phone2') . '-' . $this->input->post('phone3'),
			);
		}
		if ($this->form_validation->run() == true && $this->ion_auth->register($username, $password, $email, $additional_data))
		{
            $this->db->where('email', $email);
            $id = $this->db->get('users');
            
            foreach($id->result() as $i)
            {
                $data = array(
                    'from' => 1, 
                    'to' => $i->id,
                    'subject' => 'Welcome to Holidaysrating.com!',
                    'content' => 'Welcome to Holidaysrating.com!'
                );
                
                $this->db->insert('personal_messages', $data);
            }
            
			$this->session->set_flashdata('message', $this->ion_auth->messages());
			redirect("auth", 'refresh');
		}
		else
		{
            $data['main']='auth/register.php';
            $data['heading']='Register';
			$this->parser->parse('template_view', $data);
		}
	}

    function my_login()
    {           
        $this->form_validation->set_rules('identity', 'Identity', 'required');
		$this->form_validation->set_rules('password', 'Password', 'required');

		if ($this->form_validation->run() == true)
		{
			$remember = (bool) $this->input->post('remember');
            
			if ($this->ion_auth->login($this->input->post('identity'), $this->input->post('password'), $remember))
			{
                // login successful
				$this->session->set_flashdata('message', $this->ion_auth->messages());
				redirect('user/index', 'refresh');
			}
            
			else
			{
                // login failed
                $data['main']='home.php';
                $data['heading']='Check Out Our Favorites';
                $data['error']=$this->ion_auth->errors();
                $this->parser->parse('template', $data);
			}
		}
		
        else
		{
			redirect('home/index', 'refresh');
		}       
    }
    
    function fb_login()
    {
        $this->user = $this->facebook->getUser();
        
        if($this->user)
        {
            try
            {
                $this->user = $this->facebook->api('/me');
                $this->logoutUrl = $this->facebook->getLogoutUrl(array("next" => site_url("auth/logout")));
            }
            catch(FacebookApiException $e)
            {
                $this->user = null;
                $this->load->view('facebook/login');
            }
        }
        
        else
        {
            $data['logoutUrl'] = $this->logoutUrl;
            $this->load->view('facebook/login', $data);
        }
    }
    
