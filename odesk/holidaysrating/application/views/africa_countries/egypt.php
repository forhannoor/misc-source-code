<p>Egypt has a hot desert climate and is generally dry. The temperatures are hot or very hot in summer and warm or mild in winter days, but warm in summer nights and cool in winter nights.
The thin coastal strip at the north of Egypt has the most moderate temperatures, because of prevailing Mediterranean Sea winds.</p>

